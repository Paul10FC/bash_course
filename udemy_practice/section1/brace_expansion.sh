#!/bin/bash

# This functionality help us to expand any thing we can find between {}
echo {jan,feb,mar,apr,may,jun} # Result: jan feb mar apr may jun
echo {1..10} # This will automaticaly write the numbers between 1 and 10
echo {10..1} # This will do the same thing writen up, but in a reverse way
echo {1..20..2} # Now we are going to write the numbers from 1 to 20 in a 2 increase value

